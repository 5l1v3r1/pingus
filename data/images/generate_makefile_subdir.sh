#!/bin/sh

TOPDIR=`pwd`

for i in $@; do
    echo "  Subdir for $i"

    cd $i && \
    rm -f Makefile.am && \
    DIRS=`find . -maxdepth 1 -type d -and -not \( -name "old" -or -name "CVS" -or -name ".*" \) | tail +1 | sed "s/^\.\///"` && \
    DIRS=`echo $DIRS` && \
    echo -e "# autogenerated file, don't touch!\n" >> Makefile.am && \
    echo "SUBDIRS = $DIRS" >> Makefile.am && \
    cd $TOPDIR
done

