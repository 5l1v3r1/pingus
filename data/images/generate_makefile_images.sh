#!/bin/sh

TOPDIR=`pwd`

for i in $@; do
    echo "  Images for $i"

    cd $i && \
    rm -f Makefile.am && \
    FILES=`find . -maxdepth 1 -type f -and \( -name "*.png" -or -name "*.jpg" \) | sed "s/^\.\///"` && \
    echo -e "# autogenerated file, don't touch!\n" >> Makefile.am && \
    echo "pkgdatadir = \$(datadir)/games/@PACKAGE@/images/$i" >> Makefile.am && \
    echo "EXTRA_DIST = " $FILES >> Makefile.am && \
    echo "pkgdata_DATA = \$(EXTRA_DIST)" >> Makefile.am && \
    echo -e "\n# EOF #\n" >> Makefile.am   && cd $TOPDIR
done

# EOF #